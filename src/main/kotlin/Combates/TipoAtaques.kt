package org.example.Combates

/**
 * Enum que representa los diferentes tipos de ataques utilizados en el combate.
 * Incluye KAMEKAMEHA, BOLAS, KAIOKEN, GOLPES, MAKANKOSAPPO, SUPERNOVA y CASTIGODIVINO.
 */
enum class TipoAtaques {
    KAMEKAMEHA,      // Ataque Kamekameha
    BOLAS,           // Ataque Bolas
    KAIOKEN,         // Ataque KaioKen
    GOLPES,          // Ataque Golpes
    MAKANKOSAPPO,    // Ataque Makankosappo
    SUPERNOVA,       // Ataque Supernova
    CASTIGODIVINO    // Ataque Castigo Divino
}
